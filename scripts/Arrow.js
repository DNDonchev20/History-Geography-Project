class Arrow{
    constructor(cx, cy, sx, sy, ex, ey, hw, hh, color){
        this.cx = cx;
        this.cy = cy;
        this.sx = sx;
        this.sy = sy;
        this.ex = ex;
        this.ey = ey;
        this.hw = hw;//arrow head width
        this.hh = hh;//arrow head height
        this.color = color;
    }
}