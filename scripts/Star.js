class Star {
  constructor(
    cx,
    cy,
    outerRadius,
    innerRadius,
    numPoints,
    lineWidth,
    stroke,
    fill,
    rotate
  ) {
    this.cx = cx;
    this.cy = cy;
    this.outerRadius = outerRadius;
    this.innerRadius = innerRadius;
    this.numPoints = numPoints;
    this.lineWidth = lineWidth;
    this.stroke = stroke;
    this.fill = fill;
    this.rotate = rotate;
  }
}
